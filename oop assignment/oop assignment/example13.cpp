#include <iostream>
#include <string>

using namespace std;

int main() {
    int age = 30;
    float pi = 3.14;
    double num = 3.1415926535;
    char letter = 'A';
    bool isHappy = true;
    string name = "Alice";

    // Output the values
    cout << "Age: " << age << endl;
    cout << "Pi (float): " << pi << endl;
    cout << "Pi (double): " << num << endl;
    cout << "Letter: " << letter << endl;
    cout << "Is Happy? " << (isHappy ? "Yes" : "No") << endl;
    cout << "Name: " << name << endl;

    return 0;
}

